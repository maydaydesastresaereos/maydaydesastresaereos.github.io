dailymotionVideos.getVideoById = function(videoId) {
    return this.allVideos.find(video => video.videoId === videoId);
};

dailymotionVideos.searchVideos = function(searchTerm) {
    const term = searchTerm.toLowerCase();
    return this.allVideos.filter(video => 
        video.text.toLowerCase().includes(term) || 
        (video.title && video.title.toLowerCase().includes(term))
    );
};

dailymotionVideos.getVideosByCategory = function(category) {
    const categories = {
        'mayday': this.categories.maydayEpisodes,
        'documentaries': this.categories.relatedDocumentaries,
        'aviation': this.categories.aviationVideos,
        'other': this.categories.otherVideos
    };
    return categories[category] || [];
};

dailymotionVideos.getEpisodesBySeason = function(seasonNumber) {
    return this.categories.maydayEpisodes.filter(episode => 
        episode.season === seasonNumber
    );
};

dailymotionVideos.getVideosWithImages = function() {
    return this.allVideos.filter(video => video.imageUrl);
};

dailymotionVideos.getVideosWithLocalImages = function() {
    return this.allVideos.filter(video => video.hasLocalImage);
};

